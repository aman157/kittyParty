package org.mz.modal;

public class KittyPartyModal {
	
	private String kittyParty;
	private String place;
	private String date;
	private String time;
	private String kittyStatus;
	
	public String getKittyStatus() {
		return kittyStatus;
	}
	public void setKittyStatus(String kittyStatus) {
		this.kittyStatus = kittyStatus;
	}
	private String budget;
	
	
	public String getKittyParty() {
		return kittyParty;
	}
	public void setKittyParty(String kittyParty) {
		this.kittyParty = kittyParty;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getBudget() {
		return budget;
	}
	public void setBudget(String budget) {
		this.budget = budget;
	}
	
	

}
